function whatIsInAName(collection, source) {
  // What's in a name?
  // filter() out the objects in collection array
  let arr = collection.filter(function(obj) {
    // Loop through properties/keys in source
    for(let key in source) {
      // Compare value of source's properties and object's properties
      if(source[key] !== obj[key]) {
        return false;
      }
    }
    return true;
  })

  return arr;
}

